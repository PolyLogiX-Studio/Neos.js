/**
 * Easy Out Var Generator
 * @class Out
 * @template T
 * @returns {[Out:T]}
 * @param {[Out:T]} Out
 */
class Out {
	constructor() {
		/**
		 * @memberof Out
		 * @instance
		 * @alias Out
		 * @type {T} */
		let self = [];
		self.Out = new Object();
		return self;
	}
}
module.exports = {
	Out,
};
